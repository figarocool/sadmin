			<div class="box-legge">
				<span>Cosa sono i cookie</span>
				<p>
					Un cookie &egrave; un piccolo file di testo che viene creato all&rsquo;interno 
					del computer di chi visualizza un sito web allo scopo di registrarvi 
					alcune informazioni relative alla visita nonch&egrave; di creare un sistema per 
					riconoscere l&rsquo;utente anche in momenti successivi; tale cookie, infatti, 
					potr&agrave; non solo essere creato ma anche letto e modificato dallo stesso sito 
					web che lo ha generato.
				</p>
				<p>
					In pratica possiamo dire che i cookie sono una sorta di "memoria" 
					attraverso la quale un sito web riesce a riconoscere uno specifico 
					utente e ad associargli delle informazioni di varia natura e per 
					differenti finalit&agrave;.
				</p>
				<p>
					E&rsquo; bene precisare che un sito Web pu&ograve; impostare un cookie sul browser 
					dell&rsquo;utente solo ed esclusivamente se le preferenze configurate per 
					quest&rsquo;ultimo lo consentono e che il browser pu&ograve; consentire a un 
					determinato sito Web di accedere solo ed esclusivamente ai cookie da 
					esso impostati e non a quelli impostati da altri siti Web in quanto, come 
					detto, il cookie &egrave; un sistema di collegamento tra uno specifico sito web 
					ed uno specifico utente.
				</p>
			</div>
			<div class="box-legge">
				<span>I cookie sono cattivi?</span>
				<p>
					No. Assolutamente no.
				</p>
				<p>
					<span>I cookie sono uno strumento tecnico essenziale per il buono e corretto 
					funzionamento di quasi tutti i siti web esistenti.</span> 
					&Egrave; bene essere molto chiari in merito perch&egrave;, a mio avviso, si sta 
					creando una grande confusione sull'argomento.
				</p>
				<p>
					Per rendercene conto &egrave; sufficiente un piccolo esperimento: proviamo a 
					bloccare - attraverso le impostazioni del browser - tutti i cookie, dopodich&egrave; 
					proviamo a navigare e ad utilizzare i nostri servizi on-line preferiti. 
					Basterebbero pochi minuti per renderci conto che, senza i cookie, non ci sarebbe 
					possibile accedere a Facebook o alla nostra webmail preferita, utilizzare il nostro 
					home-banking ne fare moltissime altre cose.
				</p>
				<p>
					La capacit&agrave; dei cookie di identificare un utente e di associargli delle 
					informazioni, infatti, &egrave; stata concepita per finalit&agrave; tecniche molto 
					importanti e non certo per poter tracciare un profilo della personalit&agrave; e delle 
					abitudini dell&rsquo;utente di Internet: quest&rsquo;ultima finalit&agrave;, in un certo 
					senso, pu&ograve; e deve essere vista come l&rsquo;eccezione alla regola, essendo i 
					cookie uno strumento nato per tutt&rsquo;altro scopo.
				</p>
			</div>
			<div class="box-legge">
				<span>Diverse tipologie di cookie</span>
				<p>
					Fortunatamente &egrave; lo stesso Garante della Privacy a rendersi 
					conto che "non tutti i cookie sono cattivi". Nel citato provvedimento 
					dell&rsquo;8 maggio 2014, infatti, il Garante identifica due macro 
					categorie di cookie:
				</p>
				<p>
					<ul>
						<li>
							<span>Cookie tecnici</span>
							<p>Si tratta di cookie normalmente trasmessi in prima persona dal 
							gestore del sito per finalità strettamente connesse al buon e 
							corretto funzionamento del sito stesso. Questa famiglia di cookie 
							comprende:
							</p>
							<ul type="circle">
								<li>
									<span>Cookie di navigazione (o di sessione)</span>
									<p>Questi cookie sono necessari per garantire la normale 
									fruizione del sito web e desi suoi servizi permettendo, 
									ad esempio, di autenticarsi ad aree riservate o di 
									effettuare un acquisto.
									</p>
								</li>
								<li>
									<span>Cookie funzionali</span>
									<p>Questi cookie, pur non essendo essenziali, migliorano 
									la funzionalit&agrave; del sito in quanto consentono 
									all&rsquo;utente di esprimere delle preferenze 
									persistenti circa alcuni aspetti della navigazione 
									(ad esempio selezionare la lingua o memorizzare alcuni 
									prodotti preferiti all&rsquo;interno di un e-commerce).
									</p>
								</li>
								<li>
									<span>Cookie di Analytics (o statistici)</span>
									<p>Sono considerati "cookie tecnici" laddove utilizzati 
									direttamente dal gestore del sito per raccogliere 
									informazioni, in forma aggregata, sul numero degli 
									utenti e su come questi visitano il sito.
									</p>
								</li>
							</ul>
						</li>
						<li>
							<span>Cookie di profilazione</span>
							<p>Riprendendo la definizione data dal garante della Privacy 
							i cookie di profilazione "sono volti a creare profili relativi 
							all&rsquo;utente e vengono utilizzati al fine di inviare messaggi 
							pubblicitari in linea con le preferenze manifestate dallo stesso 
							nell&rsquo;ambito della navigazione in rete".
							</p>
						</li>
					</ul>
				</p>
				<p>
					Mentre per i cookie tecnici NON &egrave; richiesto alcun consenso 
					preventivo da parte dell&rsquo;utente 
					(il sito web, cio&egrave;, pu&ograve; gestirli tranquillamente senza dover 
					adempiere ad alcuna formalit&agrave;), 
					<span>per i cookie di profilazione la normativa europea e italiana prevede 
					che "l&rsquo;utente debba essere adeguatamente informato sull&rsquo;uso degli stessi ed 
					esprimere cos&igrave; il proprio valido consenso".
					</span>
				</p>
			</div>
			<div class="box-legge">
				<span>Cookie di prime e terze parti</span>
				<p>
					Un&rsquo;altra importante distinzione riguarda il soggetto attivo, 
					cio&egrave; colui il quale procede alla materiale trasmissione 
					del cookie sul computer dell&rsquo;utente. In questo senso si 
					distingue tra i cosiddetti cookie di prime parti e di terze parti:
				</p>
				<p>
					<ul>
						<li>
							<p>i cookie di "prime parti" sono, in poche parole, quelli 
							trasmessi direttamene dal gestore del sito web visitato;
							</p>
						</li>
						<li>
							<p>i cookie di "terze parti" sono, invece, quelli trasmessi da 
							societ&agrave; terze presenti all&rsquo;interno del sito visitato 
							mediante appositi codici, i quali sono stati inclusi al fine 
							di integrare nel sito funzionalit&agrave; ulteriori.
							</p>
						</li>
					</ul>
				</p>
				<p>
					Come &egrave; facile immaginare, i cookie di terze parti sono quelli 
					che suscitano le maggiori preoccupazioni in quanto, oltre ad essere 
					incredibilmente diffusi, sono totalmente estranei alla gestione ed al 
					controllo del titolare del sito che, spesso e volentieri, ne ignora le 
					finalit&agrave; se non addirittura l&rsquo;esistenza.
				</p>
				<p>
					Per fare un esempio, si pensi che qualunque sito abbia integrato un 
					sistema di statistiche come quello di Google Analytics o un plugin 
					sociale come quello di Facebook (si pensi al classico pulsante "Mi piace") 
					trasmette, spesso senza nemmeno saperlo, dei cookie di terze parti ai 
					propri utenti fungendo da tramite mediante le proprie pagine web.
				</p>
				<p>
					La distinzione descritta tra cookie di prime e terze parti assume, 
					come vedremo in seguito, particolare rilievo anche all&rsquo;interno 
					della cookie law.
				</p>
			</div>
			<div class="box-legge">
				<span>Cosa prevede la normativa italiana</span>
				<p>
					Il Codice in materia di protezione dei dati personali 
					(anche detto Codice della Privacy) prevede, all&rsquo;articolo 122 comma 1, 
					che "l&rsquo;archiviazione delle informazioni nell&rsquo;apparecchio terminale di 
					un contraente o di un utente o l&rsquo;accesso a informazioni gi&agrave; archiviate 
					sono consentiti unicamente a condizione che il contraente o l&rsquo;utente abbia 
					espresso il proprio consenso dopo essere stato informato (...)".
				</p>
				<p>
					In altre parole possiamo dire che l&rsquo;utilizzo dei cookie 
					all&rsquo;interno di un sito web deve essere preventivamente accettato 
					dall&rsquo;utente, il quale deve esprimere il proprio consenso informato 
					(tale consenso, come gi&agrave; detto, NON &egrave; necessario qualora un 
					sito utilizzi esclusivamente "cookie tecnici").
				</p>
				<p>
					Particolarmente importante, nel contesto della normativa sui cookie, 
					&egrave; il concetto di "informativa": abbiamo detto infatti che prima di 
					poter prestare validamente il proprio consenso, l&rsquo;utente deve essere 
					adeguatamente informato circa l&rsquo;utilizzo che il sito fa dei cookie. 
					Ciò, sempre secondo le disposizioni del garante, deve avvenire in due 
					fasi distinte.
				</p>
			</div>
			<div class="box-legge">
				<span>L&rsquo;informativa breve</span>
				<p>
					Il garante ha stabilito che nel momento in cui un utente accede al 
					sito web (non &egrave; importante che ci&ograve; accada in home-page o 
					in una pagina interna), gli venga subito mostrato un breve avviso 
					circa il fatto che il sito utilizza cookie. Da questa prima affermazione 
					possiamo già capire che l&rsquo;avviso deve essere implementato 
					all&rsquo;interno di tutte le pagine pubblicamente accessibili del sito.
				</p>
				<p>
					Tale avviso pu&ograve; essere mostrato utilizzando un "banner di idonee dimensioni" 
					in modo tale da rendere una "percettibile discontinuità nella fruizione dei contenuti 
					della pagina web che si sta visitando". In altre parole il banner deve essere visibile 
					e non confondersi con gli altri contenuti del sito.
				</p>
				<p>
					Tale banner deve contenere obbligatoriamente:
				</p>
				<p>
					<ul>
						<li>
							<p>un avviso circa l&rsquo;utilizzo dei cookie di profilazione, 
							precisando, qualora ci&ograve; accada, che tali cookie possono 
							essere trasmessi anche da "terze parti";
							</p>
						</li>
						<li>
							<p>un link alla pagina contenente l&rsquo;informativa estesa 
							(la cosiddetta cookie policy);
							</p>
						</li>
						<li>
							<p>l&rsquo;indicazione circa la facoltativit&agrave; del cosenso;</p>
						</li>
						<li>
							<p>l&rsquo;indicazione che la prosecuzione della navigazione o 
							l&rsquo;interazione con la pagina (come l&rsquo;esecuzione di un attivit&agrave; 
							di scrolling dei contenuti o il click su un elemento) comporta 
							la prestazione del consenso all&rsquo;uso dei cookie.
							</p>
						</li>
					</ul>
				</p>
				<p>
					Tale banner pu&ograve; essere nascosto all&rsquo;utente solo dopo che questi 
					abbia espressamente manifestato il proprio consenso all&rsquo;utilizzo dei 
					cookie (ad esempio &egrave; possibile integrare, all&rsquo;interno del banner stesso, 
					un bottone di conferma che, una volta premuto, comporta la scomparsa 
					dell&rsquo;informativa breve).
				</p>
				<p>
					&Egrave; consentito registrare tale consenso all&rsquo;interno di un cookie 
					tecnico in modo tale da non mostrare nuovamete l&rsquo;informativa agli utenti 
					che hanno gi&agrave; espresso la loro intenzione di acconsentire all&rsquo;utilizzo 
					dei cookie.
				</p>
				<p>
					Sempre all&rsquo;interno dell&rsquo;informativa estesa deve essere indicata la 
					possibilit&agrave; per l&rsquo;utente di esprimere le proprie preferenze in 
					merito ai cookie ed alla loro disattivazione: ci&ograve; pu&ograve; essere fatto 
					anche mediante la semplice indicazione delle procedure necessarie 
					al blocco totale e/o selettivo dei cookie mediante le impostazioni 
					del browser di navigazione. In tal senso pu&ograve; ritenersi sufficiente 
					anche l&rsquo;inserimento dei link alle relative pagine d&rsquo;istruzioni fornite 
					dai produttori dei software.
				</p>
				<p>
					&Egrave; molto importante ricordare, infine, che questa informativa 
					sia raggiungibile facilmente da parte degli utenti: 
					1) mediante un link all&rsquo;interno dell&rsquo;informativa breve; 
					2) mediante un link presente in calce ad ogni pagina del sito web.
				</p>
			</div>
			<div class="box-legge">
				<span>Il grosso problema: il blocco dei cookie prima del consenso</span>
				<p>
					Il grosso problema della normativa consiste nel fatto che non &egrave; 
					permesso trasmettere un cookie (non tecnico) all&rsquo;utente sino a quando 
					questi non abbia prestato il proprio consenso: il altre parole, il 
					consenso deve essere necessariamente preventivo all&rsquo;invio dei cookie.
				</p>
				<p>
					Come abbiamo detto, il consenso dell&rsquo;utente pu&ograve; essere 
					considerato dato anche a seguito di un gesto d&rsquo;interazione con la pagina 
					(ad esempio mediante uno scrolling dei contenuti), ma sino a quando 
					l&rsquo;utente non compie nessun atto di accettazione espressa 
					(click su eventuale bottone presente all&rsquo;interno dell&rsquo;informativa breve) 
					o prosegue nella navigazione o interazione col sito, i cookie non 
					tecnici NON possono essere trasmessi.
				</p>
				<p>
					A questo punto sorge un dubbio non chiarito all&rsquo;interno della normativa: 
					se il mio sito trasmette cookie di profilazione di terze parti 
					sono obbligato a bloccarli in attesa del consenso dell'utente?
				</p>
				<p>
					Se cos&igrave; fosse, per fare un esempio, ogni pagina che ospita dei banner 
					di Google AdSense dovrebbe bloccarli (quindi non mostrarli) sino a 
					quando non si registra l&rsquo;accettazione dell&rsquo;utente nei modi indicati 
					in precedenza.
				</p>
				<p>
					Questo, purtroppo, &egrave; l&rsquo;aspetto pi&ugrave; complesso ed oscuro della normativa 
					in quanto l&rsquo;adeguamento tecnico in tal senso, se tale orientamento verr&agrave; 
					confermato, sar&agrave; molto complesso ed effettivamente di difficile 
					implementazione per chi non possiede le giuste conoscenze tecniche. 
					Il rischio, di fatto, &egrave; quello di colpire molto duramente la piccolissima 
					editoria che potrebbe ritrovarsi costretta a rinunciare ad una buona 
					fetta dei propri guadagni (molti blog "vivono di AdSense") non potendo 
					mostrare i banner alla prima visita dell&rsquo;utente!
				</p>
				<p>
					Se tale orientamento fosse confermato, inoltre, tutti i siti web 
					sarebbero chiamati a ripensare a buona parte dei propri contenuti 
					escludendo o limitando contenuti di terze parti come video, mappe e 
					plugin sociali.
				</p>
			</div>
			<div class="box-legge">
				<span>Notificazione al garante circa l'utilizzo di cookie di profilazione</span>
				<p>
					Un ulteriore adempimento previsto dalla normativa riguarda i soggetti 
					che utilizzano cookie di profilazione. Tali soggetti, infatti, sono 
					chiamati ad effettuare una notifica al Garante come disposto dall&rsquo;art. 
					37 del Codice.
				</p>
				<p>
					&Egrave; bene precisare che sono esentati da tale adempimento tutti i siti 
					che non facciano uso in maniera diretta di cookie per la profilazione 
					dell&rsquo;utenza: sono esclusi dall&rsquo;obbligo di notifica, quindi, i siti che 
					trasmettono cookie di profilazione di "terze parti" o che utilizzano 
					direttamente cookie tecnici per i quali, ricordiamo nuovamente, non &egrave; 
					richiesto alcun adempimento.
				</p>
			</div>
			<div class="box-legge">
				<span>Conseguenze del mancato adeguamento agli obblighi previsti dalla "cookie law"</span>
				<p>
					Il mancato e/o non corretto adeguamento alle disposizioni sancite 
					dalla nuova normativa sui cookie pu&ograve; avere delle conseguenze 
					davvero pesantissime. Il provvedimento dell&rsquo;8 maggio 2014, infatti, 
					prevede sanzioni amministrative salatissime da un minimo di 6.000 
					fino ad un massimo di 36.000 Euro per chi non &egrave; in regola.
				</p>
				<p>
					Ancora peggiori le conseguenze dell&rsquo;installazione di cookie di 
					profilazione senza il consenso dell&rsquo;interessato: in questo caso, 
					infatti, la sanzione va da un minimo di 10.000 ad un massimo di 
					120.000 Euro. La mancata notificazione al Garante, infine, prevede una 
					sanzione da 20.000 a 120.000 Euro.
				</p>
			</div>
			<div class="box-legge">
				<span>Considerazioni sulla normativa</span>
				<p>
					Seppur motivata dal lodevole intento di tutelare la privacy degli 
					utenti della Rete, la cookie law, soprattutto nella versione italiana, 
					rischia di essere un vero e proprio terremoto per l'ecosistema dei 
					siti web nostrani: se l&rsquo;avviso appare giustificato nel caso in cui 
					un sito adotti in prima persona dei cookie di tracciamento, forse 
					eccessivamente oneroso appare l&rsquo;obbligo di informativa ed il blocco 
					preventivo per i cookie di terze parti spesso ospitati inconsapevolmente 
					sui siti di migliaia di piccoli e piccolissimi editori o di semplici 
					blogger.
				</p>
				<p>
					Per questa particolare categoria, infatti, non c&rsquo;&egrave; solo il rischio di 
					una forte riduzione dei guadagni (pensiamo al blocco dei banner sino 
					all&rsquo;accettazione dei cookie) ma anche quello, forse pi&ugrave; grave, di dover 
					(o voler) chiudere bottega: nei prossimi mesi, infatti, potrebbero essere 
					non pochi i gestori di siti web che, ritenendo troppo rischioso, 
					troppo complesso e/o economicamente non pi&ugrave; conveniente la permanenza 
					on-line, decideranno di interrompere le pubblicazioni.
				</p>
				<p>
					In tal senso &egrave; auspicabile che il Garante intervenga attutendo 
					l&rsquo;impatto della normativa o che i colossi della Rete (Google in primis) 
					adattino i propri strumenti alla normativa italiana, liberando gli 
					editori dal rischio di incorrere in sanzioni per attivit&agrave; non a 
					loro direttamente riconducibili.
				</p>
